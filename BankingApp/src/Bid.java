
public class Bid {
	private int id;
	private float bidprice;
	
	
	public Bid()
	{
		
	}


	public int getId() {
		return id;
	}


	public void setId(int id) {
		this.id = id;
	}


	public float getBidprice() {
		return bidprice;
	}


	public void setBidprice(float bidprice) {
		this.bidprice = bidprice;
	}
	
	

}
